// Números de Ponto Flutuante:

// Para declarar um número do tipo float (decimal) usamos 'float'

// A mascára do tipo inteiro é o '%f'

// O tipo float ocupa 4 bytes na memória

#include <stdio.h>

int main()
{
    float num;
    num = 10.57;

    printf("Valor da váriavel num (ponto flutuante) = %f\n", num);

    return 0;
}
