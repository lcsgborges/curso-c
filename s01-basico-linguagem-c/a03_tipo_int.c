// Números Inteiros:

// Para declarar um número do tipo inteiro usamos 'int'

// A mascára do tipo inteiro é o '%d'

// O tipo int ocupa 4 bytes na memória

#include <stdio.h>

int main()
{
    int num; 
    num = 50;

    printf("Valor da variável num = %d\n", num);

    num = 100;

    printf("Novo valor da váriavel num = %d\n", num);

    return 0;
}
