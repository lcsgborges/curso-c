#include <stdio.h>

// Operador de resto da divisão (%):

int main (){

    // resto da divisão (%), exemplo, resto da divisão de 10 e 7 tem que ser 3.

    int a = 10, b = 7;

    int resto = a % b;

    printf("Resto da divisão: %d\n", resto); 
    
    // podemos usar isso para verificar se um numero é par ou impar.

    return 0;
}